#include <stdio.h>
#include <stdlib.h>

int main()
{
  char k1 = 'A';
  char k2 = 66;
  int k3 = 'C';
  int k4 = k1+2;

  printf("k1 = %c  %d \n\n", k1, k1);
  printf("k2 = %c  %d \n\n", k2, k2);
  printf("k3 = %c  %d \n\n", k3, k3);
  printf("k4 = %c  %d \n\n", k4, k4);

  system("PAUSE");
  return 0;
}
